public class Vehiculo {
    private int id;
    private int idMarca;
    private int idProveedor;
    private int idTipoVehiculo;
    private int idTipoTransmision;
    private int idTipoMotor;
    private String vin;
    private String color;
    private int anioFabricacion;
    private String placa;
    private int anioMatricula;
    private double precioCosto;
    private double precioVenta;
    
    

    // Getters y Setters
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getIdMarca() {
        return idMarca;
    }
    public void setIdMarca(int idMarca) {
        this.idMarca = idMarca;
    }
    public int getIdProveedor() {
        return idProveedor;
    }
    public void setIdProveedor(int idProveedor) {
        this.idProveedor = idProveedor;
    }
    public int getIdTipoVehiculo() {
        return idTipoVehiculo;
    }
    public void setIdTipoVehiculo(int idTipoVehiculo) {
        this.idTipoVehiculo = idTipoVehiculo;
    }
    public int getIdTipoTransmision() {
        return idTipoTransmision;
    }
    public void setIdTipoTransmision(int idTipoTransmision) {
        this.idTipoTransmision = idTipoTransmision;
    }
    public int getIdTipoMotor() {
        return idTipoMotor;
    }
    public void setIdTipoMotor(int idTipoMotor) {
        this.idTipoMotor = idTipoMotor;
    }
    public String getVin() {
        return vin;
    }
    public void setVin(String vin) {
        this.vin = vin;
    }
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
    public int getAnioFabricacion() {
        return anioFabricacion;
    }
    public void setAnioFabricacion(int anioFabricacion) {
        this.anioFabricacion = anioFabricacion;
    }
    public String getPlaca() {
        return placa;
    }
    public void setPlaca(String placa) {
        this.placa = placa;
    }
    public int getAnioMatricula() {
        return anioMatricula;
    }
    public void setAnioMatricula(int anioMatricula) {
        this.anioMatricula = anioMatricula;
    }
    public double getPrecioCosto() {
        return precioCosto;
    }
    public void setPrecioCosto(double precioCosto) {
        this.precioCosto = precioCosto;
    }
    public double getPrecioVenta() {
        return precioVenta;
    }
    public void setPrecioVenta(double precioVenta) {
        this.precioVenta = precioVenta;
    }
}
